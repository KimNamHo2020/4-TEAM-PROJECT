CREATE SCHEMA 도서관_좌석_예약_시스템

USE 도서관_좌석_예약_시스템


CREATE TABLE 권한 (
    권한_ID INT PRIMARY KEY,
    권한 VARCHAR(50)
);

CREATE TABLE 시스템 (
    시스템_ID INT PRIMARY KEY,
    권한_ID INT,
    보안_관리 VARCHAR(255),
    업데이트_및_유지보수 VARCHAR(255),
    시스템_비밀번호 VARCHAR(255),
    FOREIGN KEY (권한_ID) REFERENCES 권한(권한_ID)
);

CREATE TABLE 도서관_휴무일 (
    휴무일_날짜 DATE PRIMARY KEY,
    휴무일_사유 VARCHAR(255)
);


CREATE TABLE 학생 (
    학번 INT PRIMARY KEY,
    시스템_ID INT,
    권한_ID INT,
    이름 VARCHAR(50),
    비밀번호 VARCHAR(255),
    이메일 VARCHAR(100),
    전화번호 VARCHAR(20),
    생성날짜 DATE,
    FOREIGN KEY (시스템_ID) REFERENCES 시스템(시스템_ID),
    FOREIGN KEY (권한_ID) REFERENCES 권한(권한_ID)
);

CREATE TABLE 좌석 (
    좌석번호 INT PRIMARY KEY,
    시스템_ID INT,
    권한_ID INT,
    위치 VARCHAR(255),
    좌석_상태 VARCHAR(50),
    FOREIGN KEY (시스템_ID) REFERENCES 시스템(시스템_ID),
    FOREIGN KEY (권한_ID) REFERENCES 권한(권한_ID)
);

CREATE TABLE 예약 (
    예약번호 INT PRIMARY KEY,
    시스템_ID INT,
    학번 INT,
    좌석번호 INT,
    휴무일_날짜 DATE,
    권한_ID INT,
    예약_가능_여부 BOOLEAN,
    예약_시작시간 DATETIME,
    예약_종료시간 DATETIME,
    예약_상태 VARCHAR(50),
    FOREIGN KEY (시스템_ID) REFERENCES 시스템(시스템_ID),
    FOREIGN KEY (학번) REFERENCES 학생(학번),
    FOREIGN KEY (좌석번호) REFERENCES 좌석(좌석번호),
    FOREIGN KEY (휴무일_날짜) REFERENCES 도서관_휴무일(휴무일_날짜),
    FOREIGN KEY (권한_ID) REFERENCES 권한(권한_ID)
);

CREATE TABLE 방 (
    방_ID INT PRIMARY KEY,
    시스템_ID INT,
    권한_ID INT,
    방_이름 VARCHAR(255),
    방_위치 VARCHAR(255),
    FOREIGN KEY (시스템_ID) REFERENCES 시스템(시스템_ID),
    FOREIGN KEY (권한_ID) REFERENCES 권한(권한_ID)
);

CREATE TABLE 활동_로그 (
    로그_ID INT PRIMARY KEY,
    시스템_ID INT,
    권한_ID INT,
    활동 VARCHAR(255),
    활동시간 DATETIME,
    세부사항 TEXT,
    FOREIGN KEY (시스템_ID) REFERENCES 시스템(시스템_ID),
    FOREIGN KEY (권한_ID) REFERENCES 권한(권한_ID)
);

CREATE TABLE 알림 (
    알림_ID INT PRIMARY KEY,
    시스템_ID INT,
    학번 INT,
    권한_ID INT,
    메세지 TEXT,
    발송된_시간 DATETIME,
    FOREIGN KEY (시스템_ID) REFERENCES 시스템(시스템_ID),
    FOREIGN KEY (학번) REFERENCES 학생(학번),
    FOREIGN KEY (권한_ID) REFERENCES 권한(권한_ID)
);

CREATE TABLE 웹사이트 (
    웹사이트_주소 VARCHAR(255) PRIMARY KEY,
    시스템_ID INT,
    권한_ID INT,
    서버 VARCHAR(255),
    FOREIGN KEY (시스템_ID) REFERENCES 시스템(시스템_ID),
    FOREIGN KEY (권한_ID) REFERENCES 권한(권한_ID)
);

